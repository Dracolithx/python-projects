# Comparison Operators
print(5 > 2)   #True
print(3 == 3)  #True
print(4 != 5)  #True

# Logical Operators
print(True and False)  #False
print(True or False)   #True
print(not True)        #False

# If-elif-else structure
score = 85

if score >= 90:
    print("Grade: A")
elif score >= 80:
    print("Grade: B")
else:
    print("Grade: C or lower")

